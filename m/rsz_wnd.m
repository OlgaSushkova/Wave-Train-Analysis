function rsz_wnd(Fig);

%---------------------------------------------------------------------%

scrsz= get(0,'ScreenSize');
K= 1;%1.25;
Width= scrsz(3)/K;
Height= scrsz(4)/K;
X= scrsz(3)/2 - Width/2;
Y= scrsz(4)/2 - Height/2;
set(figure(Fig),'Position',[X,Y,Width,Height]);

